    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String NomePaciente = jTextField1.getText();
        String RGPaciente = jTextField2.getText();
        String CPFPaciente = jTextField3.getText();
        String EnderecoPaciente = jTextField4.getText();
        String FonePaciente = jTextField5.getText();
        String NascPaciente = jTextField6.getText();

        Paciente umPaciente = new Paciente();
        umPaciente.setNomePaciente(NomePaciente);
        umPaciente.setRGPaciente(RGPaciente);
        umPaciente.setCPFPaciente(CPFPaciente);
        umPaciente.setEnderecoPaciente(EnderecoPaciente);
        umPaciente.setFonePaciente(FonePaciente);
        umPaciente.setNascPaciente(NascPaciente);

        BD novoBD = new BD();
        novoBD.incluirPaciente(NomePaciente, RGPaciente, CPFPaciente, EnderecoPaciente, FonePaciente, NascPaciente);
        JOptionPane.showMessageDialog(null, "Inclusão Confirmada");
    }//GEN-LAST:event_jButton1ActionPerformed

