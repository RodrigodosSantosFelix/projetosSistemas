public class Paciente {

    private String NomePaciente;
    private String RGPaciente;
    private String CPFPaciente;
    private String EnderecoPaciente;
    private String FonePaciente;
    private String NascPaciente;

    public String getNomePaciente() {
        return NomePaciente;
    }

    public void setNomePaciente(String NomePaciente) {
        this.NomePaciente = NomePaciente;
    }

    public String getRGPaciente() {
        return RGPaciente;
    }

    public void setRGPaciente(String RGPaciente) {
        this.RGPaciente = RGPaciente;
    }

    public String getCPFPaciente() {
        return CPFPaciente;
    }

    public void setCPFPaciente(String CPFPaciente) {
        this.CPFPaciente = CPFPaciente;
    }

    public String getEnderecoPaciente() {
        return EnderecoPaciente;
    }

    public void setEnderecoPaciente(String EnderecoPaciente) {
        this.EnderecoPaciente = EnderecoPaciente;
    }

    public String getFonePaciente() {
        return FonePaciente;
    }

    public void setFonePaciente(String FonePaciente) {
        this.FonePaciente = FonePaciente;
    }

    public String getNascPaciente() {
        return NascPaciente;
    }

    public void setNascPaciente(String NascPaciente) {
        this.NascPaciente = NascPaciente;
    }
}
